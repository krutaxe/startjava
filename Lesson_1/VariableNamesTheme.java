public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");

        // Цифра
        int digit = 3;
        System.out.println("Цифра = " + digit);

        // Сумма цифр
        int sumDigits = 5;
        System.out.println("Сумма чисел = " + sumDigits);

        // Произведение цифр
        int multiplicationDigits = 25;
        System.out.println("Произведение цифр = " + multiplicationDigits);

        // Максимальное число
        int maxNumber = 99;
        System.out.println("Максимальное число = " + maxNumber);

        // Колличество десятков
        int numberOfTens = 6;
        System.out.println("Колличество десятков = " + numberOfTens);

        // Вес собаки
        float dogWeight = 9.4f;
        System.out.println("Вес собаки = " + dogWeight);

        // Исходное число
        int originalNumber = 10;
        System.out.println("Исходное число = " + originalNumber);

        // Процент по вкладу
        int rateOnDeposit = 16;
        System.out.println("Процент по вкладу = " + rateOnDeposit);

        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ & = " + ampersand);

        // Код ошибки
        int errorCode = 101;
        System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        int typeMessage = 100;
        System.out.println("Тип сообщения = " + typeMessage);

        // Число нулей
        int numberOfZeros = 0;
        System.out.println("Число нулей = " + numberOfZeros);

        // Уникальное число
        int uniqueNumber = -4;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Математическое выражение
        int mathExpression = 1 + 2 + 3;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        int gameScore = 5;
        System.out.println("Счет в игре = " + gameScore);

        // Пунтк меню
        int menuItem = 3;
        System.out.println("Пунтк меню = " + menuItem);

        // Стоимость кофе
        double coffeeCost = 15.45;
        System.out.println("Стоимость кофе = " + coffeeCost);

        // Дата начала
        int startDate = 11;
        System.out.println("Дата начала = " + startDate);

        // Oкончание диапазона
        int endOfRange = 90;
        System.out.println("Окончание диапазона = " + endOfRange);

        // Полное имя работника месяца
        char fullNameOfEmployeeMonth = 'M';
        System.out.println("Полное имя работника месяца = " + fullNameOfEmployeeMonth);

        // Заголовок электронной книги
        int ebookTitle = 1001;
        System.out.println("Заголовок электронной книги = " + ebookTitle);

        // Размер
        int size = 100000;
        System.out.println("Размер = " + size);

        // Вместимость
        long capacity = 1000000000;
        System.out.println("Вместимость = " + capacity);

        // Счетчик
        int counter = 55;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        int pathToFile = 10011001;
        System.out.println("Путь до файла = " + pathToFile);

        // Количество чисел в каждой строке
        int numberOfNumbersInEachLine = 999;
        System.out.println("Количество чисел в каждой строке = " + numberOfNumbersInEachLine);

        System.out.println("2. boolean-переменные");

        // Сотни равны?
        boolean isEqualHundred = true;
        System.out.println("Сотни равны? " + isEqualHundred);

        // Компьютер включён?
        boolean computerIsOn = true;
        System.out.println("Компьютер включён? " + computerIsOn);

        // Есть равные цифры?
        boolean hasUniqueNumbers = false;
        System.out.println("Есть равные цифры? " + hasUniqueNumbers);

        // Создано?
        boolean isCreated = true;
        System.out.println("Создано? " + isCreated);

        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное? " + isActive);

        // Новое?
        boolean isNew = true;
        System.out.println("Новое? " + isNew);

        // Электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? " + isValidEmail);

        // Имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("Имеются уникальные строки? " + hasUniqueStrings);

        System.out.println("3. Аббревиатуры");

        // Старый universally unique identifier
        int oldUuId = 462825;
        System.out.println("Старый universally unique identifier " + oldUuId);

        // Производитель оперативной памяти
        int manufacturerRam = 777;
        System.out.println("Производитель оперативной памяти " + manufacturerRam);

        // Ёмкость жесткого диска
        int capacityHdd = 512;
        System.out.println("Ёмкость жесткого диска " + capacityHdd);

        // Протокол передачи гипертекста
        int http = 1;
        System.out.println("Протокол передачи гипертекста " + http);

        // Сокращенный uniform resource locator
        int shortUrl = 2;
        System.out.println("Сокращенный uniform resource locator " + shortUrl);

        // Новый идентификатор клиента
        int newClientId = 999;
        System.out.println("Новый идентификатор клиента " + newClientId);

        // American standard code for information interchange
        int ascii = 10;
        System.out.println("American standard code for information interchange " + ascii);
    }
}